    private void btnIngresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresoActionPerformed
        // TODO add your handling code here:
        System.out.println(Registro.cuentas.recorrerUsuarios());
//      
        String usr = tfUser.getText();
        String pass = tfPass.getText();
        
        if(Registro.cuentas.listaVacia()){ // si no hay cuentas
            JOptionPane.showMessageDialog(this, "No hay usuarios registrados");
        }else if(Registro.cuentas.buscarUsuario(usr, pass)){
            this.setVisible(false);
            Principal ventanaPrincipal = new Principal(Registro.cuentas, this);
            ventanaPrincipal.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Datos no encontrados");
        }
        
    }//GEN-LAST:event_btnIngresoActionPerformed

